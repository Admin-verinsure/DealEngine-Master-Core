    public class NZACSPIUWModule : IUnderwritingModule
    {
        public string Name { get; protected set; }

        public NZACSPIUWModule()
        {
            Name = "NZACS_PI";
        }

        public bool Underwrite(User CurrentUser, ClientInformationSheet informationSheet)
